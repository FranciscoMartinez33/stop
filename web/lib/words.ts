function normalizeWord(word: string): string {
  const MAP: Record<string, string> = {
    "À":"A","Á":"A","Â":"A","Ã":"A","Ä":"A","Å":"A",
    "à":"A","á":"A","â":"A","ã":"A","ä":"A","å":"A",
    "È":"E","É":"E","Ê":"E","Ë":"E",
    "è":"E","é":"E","ê":"E","ë":"E",
    "Ì":"I","Í":"I","Î":"I","Ï":"I",
    "ì":"I","í":"I","î":"I","ï":"I",
    "Ò":"O","Ó":"O","Ô":"O","Õ":"O","Ö":"O",
    "ò":"O","ó":"O","ô":"O","õ":"O","ö":"O",
    "Ù":"U","Ú":"U","Û":"U","Ü":"U",
    "ù":"U","ú":"U","û":"U","ü":"U",
    "Ñ":"N","ñ":"N","Ç":"C","ç":"C",
    "Ý":"Y","ý":"Y","Š":"S","š":"S","Ś":"S","ś":"S",
    "Ž":"Z","ž":"Z","Ź":"Z","ź":"Z","Ř":"R","ř":"R",
    "Č":"C","č":"C","Ć":"C","ć":"C","Ě":"E","ě":"E",
    "Ď":"D","ď":"D","Ľ":"L","ľ":"L","Ń":"N","ń":"N",
    "İ":"I","ı":"I",
  };
  return word.toUpperCase().split("").map(ch => MAP[ch] ?? ch).join("");
}

export function hasWord(topic: string, word: string): boolean {
  const key = topic.trim().toUpperCase();
  const list = WORDS[key];
  if (!list) return true; // unknown topics always pass
  const norm = normalizeWord(word.trim());
  return list.some(e => normalizeWord(e) === norm);
}

